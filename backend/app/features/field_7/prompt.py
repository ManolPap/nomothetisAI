"""Prompt templates για το Πεδίο 7 — 2-call αντιστοίχιση νόμου με SDGs.

Call 1: Επιλογή SDGs — μόνο τίτλοι (17 γραμμές) + περίληψη νόμου → λίστα IDs.
Call 2: Εύρεση υποστόχων — μόνο υποστόχοι των επιλεγμένων SDGs + περίληψη νόμου → JSON με reasoning.
"""

from app.features.field_7.sdg_data import SDG_DATA

# Κόβουμε το κείμενο νόμου και στις δύο κλήσεις για να μειώσουμε το context.
LAW_TEXT_LIMIT = 3000


# -------------------------------------------------------
# Helpers — κατασκευή SDG context ανά call
# -------------------------------------------------------

def build_sdg_titles_text() -> str:
    """Λίστα μόνο των 17 τίτλων SDGs (χωρίς υποστόχους) για το Call 1."""
    lines: list[str] = []
    for sdg_id in sorted(SDG_DATA.keys()):
        lines.append(f"SDG {sdg_id}: {SDG_DATA[sdg_id]['title']}")
    return "\n".join(lines)


def build_subtargets_text(sdg_ids: list[int]) -> str:
    """Λίστα υποστόχων μόνο για τα SDGs που έχουν επιλεγεί στο Call 1."""
    lines: list[str] = []
    for sdg_id in sorted(set(sdg_ids)):
        sdg = SDG_DATA.get(sdg_id)
        if not sdg:
            continue
        lines.append(f"\nSDG {sdg_id}: {sdg['title']}")
        for code, title in sdg["subtargets"].items():
            lines.append(f"  {code}: {title}")
    return "\n".join(lines)


