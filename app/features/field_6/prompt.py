def build_synthesis_human(
    topic: str,
    measures: str,
    sector: str,
    ministry: str,
    facts_text: str,
    eurostat_text: str = "",
) -> str:
    eurostat_section = (
        f"ΜΕΤΡΗΣΙΜΟΙ ΔΕΙΚΤΕΣ ΑΠΟ EUROSTAT "
        f"(για χώρες που εφάρμοσαν συναφείς πρακτικές):\n{eurostat_text}\n\n"
        if eurostat_text
        else ""
    )
    return SYNTHESIS_HUMAN_TEMPLATE.format(
        topic=topic,
        measures=measures,
        sector=sector,
        ministry=ministry,
        facts_text=facts_text,
        eurostat_section=eurostat_section,
        few_shot_examples=FEW_SHOT_EXAMPLES,
    )
